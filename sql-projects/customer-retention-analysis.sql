Customer Retention & Churn Analysis (SQL Case Study)

This project demonstrates a full end‑to‑end SQL analysis focused on customer retention, churn behavior, and monthly revenue patterns. It simulates a realistic analytics environment where a business needs to understand how customers engage over time, identify churn events, and quantify retention performance.

The goal of this case study is to showcase practical SQL skills used in mid‑level data and insights analyst roles, including:
- Data cleaning and standardization
- Joining and aggregating transactional data
- Building business logic for “active” vs. “churned” customers
- Using window functions to compare customer activity month‑over‑month
- Creating a monthly churn summary with clear, interpretable metrics

    Business Problem
A subscription‑based company wants to understand:
- How many customers remain active each month
- When churn events occur
- How churn impacts monthly customer counts
- Which months show the highest or lowest retention

This analysis helps stakeholders make decisions about customer engagement, product improvements, and revenue forecasting.

    Technical Skills Demonstrated
- CTE (Common Table Expression) structuring for readable SQL
- Data cleaning using TRIM, LOWER, and COALESCE
- Revenue aggregation using SUM and DATE_TRUNC
- Window functions (LAG) to detect churn transitions
- Conditional logic for churn classification
- Monthly KPI calculation (churn rate, active customers)

    Output
The final query produces a month‑by‑month retention summary including:
- Total customers
- Number of churned customers
- Monthly churn rate (percentage)

This project reflects real‑world SQL tasks performed in analytics, insights, and business intelligence roles.

